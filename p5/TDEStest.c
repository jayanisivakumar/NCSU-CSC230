/** Return true if A and B contain the same sequence of n bytes. */
bool cmpBytes( byte const A[], byte const B[], int n )
{
  for ( int i = 0; i < n; i++ )
    if ( A[ i ] != B[ i ] )
      return false;
  return true;
}

